package com.nextgenrail.api.model;

/**
 * Train type enumeration representing different categories of trains
 */
public enum TrainType {
    SUPERFAST,      // Superfast Express trains
    EXPRESS,        // Express trains
    PASSENGER,      // Passenger trains
    RAJDHANI,       // Rajdhani Express (premium long-distance)
    SHATABDI,       // Shatabdi Express (day-time intercity)
    VANDE_BHARAT,   // Vande Bharat Express (semi-high-speed)
    DURONTO,        // Duronto Express (non-stop long-distance)
    GARIB_RATH      // Garib Rath (affordable AC trains)
}